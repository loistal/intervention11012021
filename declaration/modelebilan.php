              <br> celui des effets non échus.
              <br> (b) Si des titres sont évalués par équivalence, ce poste est subdivisé en deux sous-postes " Participations évaluées par équivalence " et " Autres
              <br> participations ". Pour les titres évalués par équivalence, la colonne " Brut " présente la valeur globale d’équivalence si elle est supérieure au
              <br> coût d’acquisition. Dans le cas contraire, le prix d’acquisition est retenu. La provision pour dépréciation globale du portefeuille figure dans la
              <br> 2ème colonne. La colonne " Net " présente la valeur globale d’équivalence positive ou une valeur nulle.
              <br> (c) A ventiler, le cas échéant, entre biens, d'une part, et services d'autre part.
              <br> (d) Créances résultant de ventes ou de prestations de services.
              <br> (e) Poste à servir directement s'il n'existe pas de rachat par l’entité de ses propres actions.
